                type: "error", 
                message: `–õ–æ–±–±–∏ –∑–∞–ø–æ–ª–Ω–µ–Ω–æ (–º–∞–∫—Å–∏–º—É–º ${MAX_PLAYERS} –∏–≥—Ä–æ–∫–æ–≤)` 
